// var change (update)
var people = 66;
people = 1010;
console.log(people);

//existing value
var apple = 22;
apple = apple + 44;
console.log(apple)

// addition to the same variable shorthand
// += means adding some value to the aexisting value
var lemon = 10;
lemon += 30;
console.log(lemon)

// ++/-- means  add -+1
var age = 50;
age++;
console.log(age);

